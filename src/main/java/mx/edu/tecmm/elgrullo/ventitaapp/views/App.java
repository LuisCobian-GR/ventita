    private void setActiveButtons(JButton selected){
        setColorButton(btnVentas, App.btnBackground, App.btnForeground);
        setColorButton(btnProdcutos, App.btnBackground, App.btnForeground);
        setColorButton(btnReportes, App.btnBackground, App.btnForeground);
        if(selected != null)
        {
            setColorButton(selected, btnBackgroundSelected, btnForegroundSelected);
        }
    }
    
    private void setColorButton(JButton button, Color background, Color foreground){
        button.setBackground(background);
        button.setForeground(foreground);
    }
    
    private void updatePanelSection(JPanel panel ){
        this.remove(pnlMain);
        pnlMain = panel; 
        this.add(pnlMain, BorderLayout.CENTER);
        this.revalidate();
        this.repaint();
    }

