    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(producto == null)
            producto = new Producto(); 
        
        producto.setCodebar(jTextField1.getText());
        producto.setDescripcion(jTextField2.getText());
        double precio = Double.parseDouble(jTextField3.getText()); 
        producto.setPrecio(precio);
        producto.setViable(true); 
        
        var res = DaoProducto.update(producto); 
        String msg = res ? "Se aplico el cambio":"Ocurrio un error";
        JOptionPane.showMessageDialog(this, msg);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

